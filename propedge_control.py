# ─────────────────────────────────────────────────────────────────────────────
class R:
    checks: list[dict] = []
    @classmethod
    def ok(cls, s, m, d=""): cls.checks.append({"st":"OK",  "s":s,"m":m,"d":d})
    @classmethod
    def warn(cls, s, m, d=""): cls.checks.append({"st":"WARN","s":s,"m":m,"d":d})
    @classmethod
    def fail(cls, s, m, d=""): cls.checks.append({"st":"FAIL","s":s,"m":m,"d":d})
    @classmethod
    def info(cls, s, m, d=""): cls.checks.append({"st":"INFO","s":s,"m":m,"d":d})
    @classmethod
    def n_ok(cls):   return sum(1 for c in cls.checks if c["st"]=="OK")
    @classmethod
    def n_warn(cls): return sum(1 for c in cls.checks if c["st"]=="WARN")
    @classmethod
    def n_fail(cls): return sum(1 for c in cls.checks if c["st"]=="FAIL")

def mb(p): 
    try: return f"{p.stat().st_size/1048576:.1f} MB"
    except: return "?"
def age_h(p):
    try: return (datetime.now().timestamp()-p.stat().st_mtime)/3600
    except: return 9999.0
def csv_rows(p):
    try:
        with open(p,'rb') as f: return sum(1 for _ in f)-1
    except: return -1
def json_plays(p):
    try:
        with open(p) as f: plays=json.load(f)
        w=sum(1 for x in plays if x.get("result")=="WIN")
        l=sum(1 for x in plays if x.get("result")=="LOSS")
        # Check if plays were generated by V1.0 (vs copied from V16)
        v1_count=sum(1 for x in plays if x.get("version","") in ("V1.0","") or True)
        return len(plays),w,l
    except: return -1,0,0


