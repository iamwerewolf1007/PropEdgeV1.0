# 8. PREDICTION ENGINE — end-to-end accuracy check
# ─────────────────────────────────────────────────────────────────────────────
def check_accuracy():
    """
    Read season JSONs and compute actual prediction accuracy.
    This is the key metric: did V1.0 predict correctly?
    BET WIN = direction was correct (not just raw WIN).
    """
    data=ROOT/"data"
    all_plays=[]
    for fname in ("season_2024_25.json","season_2025_26.json"):
        p=data/fname
        if not p.exists(): continue
        try:
            with open(p) as f: plays=json.load(f)
            all_plays.extend(plays)
        except: pass

    if not all_plays:
        R.warn("ACCURACY","No season JSON data found — run: python3 run.py generate")
        return

    # Filter to graded plays only
    graded=[p for p in all_plays if p.get("result") in ("WIN","LOSS")]
    if not graded:
        R.warn("ACCURACY","No graded plays found in season JSONs")
        return

    # Compute BET WIN correctly
    def is_bet_win(p):
        direction=str(p.get("direction",p.get("dir","OVER"))or"OVER").upper()
        result=str(p.get("result",""))
        return result=="LOSS" if "UNDER" in direction else result=="WIN"

    bet_wins=[p for p in graded if is_bet_win(p)]
    baseline=len(bet_wins)/len(graded)

    R.info("ACCURACY",
           f"Total graded plays: {len(graded):,} | Baseline BET WIN rate: {baseline:.1%}")

    # Tier breakdown using stored elite_prob
    tiers=[
        ("APEX",   0.90, "≥0.90"),
        ("ULTRA",  0.85, "≥0.85"),
        ("ELITE",  0.78, "≥0.78"),
        ("STRONG", 0.72, "≥0.72"),
        ("PLAY+",  0.65, "≥0.65"),
    ]
    targets={"APEX":0.95,"ULTRA":0.90,"ELITE":0.85,"STRONG":0.80,"PLAY+":0.75}

    any_tier_data=False
    for tier_name,thresh,label in tiers:
        tier_plays=[p for p in graded
                    if float(p.get("elite_prob",p.get("conf",0)))>=thresh]
        if len(tier_plays)<5: continue
        any_tier_data=True
        tier_wins=[p for p in tier_plays if is_bet_win(p)]
        hr=len(tier_wins)/len(tier_plays)
        target=targets[tier_name]
        msg=f"{tier_name} ({label}): {hr:.1%} | n={len(tier_plays):,}"
        if hr>=target:
            R.ok("ACCURACY",msg,f"✓ target={target:.0%} MET")
        elif hr>=target-0.05:
            R.warn("ACCURACY",msg,f"⚠ target={target:.0%} CLOSE (within 5pp)")
        else:
            R.warn("ACCURACY",msg,f"✗ target={target:.0%} NOT YET MET — need more graded data")

    if not any_tier_data:
        R.warn("ACCURACY","Insufficient plays in confidence tiers — need more graded data",
               "Run: python3 run.py generate to score and grade all plays")

    # Sanity: check grading direction
    raw_wins=[p for p in graded if p.get("result")=="WIN"]
    raw_win_rate=len(raw_wins)/len(graded)
    if abs(raw_win_rate-0.465)>0.05:
        R.warn("ACCURACY",
               f"Raw WIN rate={raw_win_rate:.1%} (unusual — expected ~46-48%)",
               "Grading might be direction-aware rather than OVER-only")
    else:
        R.ok("ACCURACY",
             f"Raw WIN rate={raw_win_rate:.1%} — consistent with OVER-only grading (46-48% expected)")


