def check_version_strings():
    stale=["V16.0","V17.0","PropEdgeV16.0","PropEdgeV17.0",
           "v17_","V17_","build_v17","FILE_V17",
           "propedge_v17.pkl","propedge_elite_v2.pkl"]
    skip={"setup_v1.py","propedge_control.py"}
    offenders=[]
    for path in sorted(ROOT.rglob("*")):
        if not path.is_file(): continue
        if path.suffix not in {'.py','.html','.md','.txt'}: continue
        if path.name in skip: continue
        if any(p.startswith('.') for p in path.parts): continue
        if '__pycache__' in str(path): continue
        try: text=path.read_text(encoding='utf-8',errors='replace')
        except: continue
        found=[s for s in stale if s in text]
        if found: offenders.append((path.relative_to(ROOT),found))
    if offenders:
        for rel,pats in offenders[:8]:
            R.fail("VERSIONS",f"{rel}: {pats}","Run: python3 setup_v1.py")
        if len(offenders)>8:
            R.fail("VERSIONS",f"...and {len(offenders)-8} more files")
    else:
        R.ok("VERSIONS","All files reference V1.0 — no stale version strings")


